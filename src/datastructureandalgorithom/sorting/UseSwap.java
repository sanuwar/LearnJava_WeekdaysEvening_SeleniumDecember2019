package datastructureandalgorithom.sorting;

public class UseSwap {

    public static void main(String[] args) {


        int num1=10;
        int num2=20;
        int temp;
        System.out.println("Number 1 "+num1);
        System.out.println("Number 2 "+num2);

        temp=num1;
        num1=num2;
        num2=temp;

        System.out.println("Number 1 "+num1);
        System.out.println("Number 2 "+num2);



    }


}
